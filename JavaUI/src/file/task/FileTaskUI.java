	private void srcButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_srcButtonActionPerformed
	{//GEN-HEADEREND:event_srcButtonActionPerformed
		initFileChooser();
		fileCooser.setCurrentDirectory(new File(srcTextField.getText()));
		if (fileCooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
		{
			srcTextField.setText(fileCooser.getSelectedFile().getAbsolutePath());
		}
	}//GEN-LAST:event_srcButtonActionPerformed

